public class GarageDoor {
    public GarageDoor() {
    }

    public void up(){
        System.out.println("Garage's door is open");
    }

    public void down(){
        System.out.println("Garage's door is close");
    }

    public void stop(){
        System.out.println("Garage's door is stop");
    }

    public void lightOn(){
        System.out.println("Garage light is on");
    }

    public void lightOff(){
        System.out.println("Garage light is off");
    }
}
